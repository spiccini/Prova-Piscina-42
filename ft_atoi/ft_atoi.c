int	ft_atoi(const char *str)
{
	int i;
	int num;
	int neg;

	neg = 1;	
	i = 0;
	num = 0;

	if(str[i] == 45)
	{
		neg = -1;
		i++;
	}
		
	while(str[i] != '\0')
	{
		if((str[i] >= 48) && (str[i] <= 57))
		{
			num = num * 10 + (str[i] - '0');
			i++;
		}
		else
		{
			num = num * neg;
			return(num);
		}
	}
	num = num * neg;
	return(num);
}
